//Using let
let age = 30; //assigned a changeable variable called age to 30 leading with 'let' 
age = 31;  //reassigned age to 31

//Using const
const birthYear = 1991; //variable assigned a value that cannot change.
birthYear = 1990; //will return an error because birthYear is not changeable. 

//Using var
var job ='engineer';
job = 'programmer'; 